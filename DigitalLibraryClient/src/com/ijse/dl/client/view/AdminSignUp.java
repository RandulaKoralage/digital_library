    private String saveUserAndMember()
            throws IOException, RemoteException, NotBoundException {
        String name = txtName.getText();
        String nic = txtNic.getText();
        String telephone = txtTelephone.getText();
        String email = txtEmail.getText();

        String userName = txtUserName.getText();
        String password = String.valueOf(pssPassword.getPassword());
        String passComf = String.valueOf(pssPassword1.getPassword());

        Date date = new Date();
        SimpleDateFormat simpledateformat
                = (SimpleDateFormat) DateFormat
                .getDateInstance(DateFormat.LONG);
        String newDate = simpledateformat.format(date);

        User user
                = new User("0", name, nic,
                        telephone, email, "Admin");
        Membership membership
                = new Membership("0", userName,
                        password, newDate, "Admin");
        String genId
                = new ControllerFactoryImpl()
                .getMemberController().addAsMember(membership, user);

        return genId;
    }
