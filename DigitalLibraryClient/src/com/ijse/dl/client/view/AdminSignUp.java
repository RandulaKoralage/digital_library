    private void lblFinishMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblFinishMouseClicked
        String password = String.valueOf(pssPassword.getPassword());
        String passComf = String.valueOf(pssPassword1.getPassword());
        if (password.equals(passComf)) {
            try {
                String genId = saveUserAndMember();
                if (genId != null) {
                    txtUserId.setText(genId);
                    JOptionPane.showMessageDialog(this,
                            "Welcome to Maze | Digital Library..."
                            + "\n" + "Your User Id is : " + genId, "Message",
                            JOptionPane.PLAIN_MESSAGE);

                } else {
                    JOptionPane.showMessageDialog(this,
                            "Sign Up Failed...", "Message",
                            JOptionPane.PLAIN_MESSAGE);
                }
            } catch (IOException ex) {
                System.err.println(ex.getMessage());
            } catch (NotBoundException ex) {
                JOptionPane.showMessageDialog(null, "Server Disconnected",
                        "Oops!",
                        JOptionPane.INFORMATION_MESSAGE, icon);
            }
        } else {
            lblError.setVisible(true);
            pssPassword.setText(null);
            pssPassword1.setText(null);
        }
    }//GEN-LAST:event_lblFinishMouseClicked

